function setLang(lang) {
    // Save to localStorage so it persists across pages
    localStorage.setItem('lang', lang);

    // Swap all tagged elements on the current page
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.textContent = translations[lang][key];
        }
    });

    // Update the language toggle button text in nav
    const langToggle = document.getElementById('lang-toggle-text');
    if (langToggle) {
        langToggle.textContent = lang === 'fr' ? 'English' : 'Français';
    }
}

// Run on every page load — reads saved language from localStorage
document.addEventListener('DOMContentLoaded', function () {
    const savedLang = localStorage.getItem('lang') || 'en';
    setLang(savedLang);
});